    private void btnCityInfoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCityInfoActionPerformed
        String message = "";
        boolean allvalid = true;
        String cityid = jtfCityId.getText().trim();
        String cityname = jtfCityName.getText().trim();
        if (jcbCountryId.getSelectedIndex() == 0) {
            message += "\n Please Select the County Id ";
            allvalid = false;
        }
        if (Validations.isEmpty(cityid)) {
            allvalid = false;
            message += "\n Please Enter the City Id ";
        }
        if (Validations.isEmpty(cityname)) {
            allvalid = false;
            message += "\n Please Enter the City Name ";
        }
        if (allvalid) {
            CityInfoTo record = new CityInfoTo();
            record.setCityid(cityid);
            record.setCityname(cityname);
            CountryInfoTo ct = (CountryInfoTo) jcbCountryId.getSelectedItem();
            record.setCountryid(ct.getCountryid());
            CityInfoDao action = new CityInfoDao();
            if (btnCityInfo.getText().contains("Add")) {
                if (action.insertRecord(record)) {
                    message = "\n City Details are Saved. ";
                } else {
                    allvalid = false;
                    message = "\n Failure occur cann't insert City Details ";
                }
            } else if (btnCityInfo.getText().contains("Update")) {
                if (action.updateRecord(record)) {
                    message = "City Details are Updated in Database";
                } else {
                    message = " Updation Failure:" + action.getErrormessage();
                }
                AllCommonOperations.openInternalFrames(getDesktopPane(), new ViewCityDetails());
                dispose();
            }

        }
        if (allvalid) {
            JOptionPane.showMessageDialog(this, message);
        } else {
            JOptionPane.showMessageDialog(this, message, "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnCityInfoActionPerformed


