    public AddCityInfo(CityInfoTo record) {
        this();
        if (record != null) {
            jtfCityId.setText(record.getCityid());
            jtfCityName.setText(record.getCityname());
            for (int i = 1; i < jcbCountryId.getItemCount(); i++) {
                CountryInfoTo ct = (CountryInfoTo) jcbCountryId.getItemAt(i);
                if(ct.getCountryid().equals(record.getCountryid())){
                    jcbCountryId.setSelectedIndex(i);
                }
            }
            setTitle("Edit Records in Database");
            btnCityInfo.setText("Update Records");
            jtfCityId.setEditable(false);

        }
    }

